namespace Imi.SupplyChain.UX.Modules.Abc
{
	public class ModuleController : ModuleControllerBase, IShellModule
	{
        public const string ModuleId = "ABC";

        private IABCUserSessionService _abcUserSessionService;
        private XmlDocument _menu;
        private bool _first = true;
                                
        public override void Run()
		{
            _abcUserSessionService = WorkItem.Services.AddNew<ABCUserSessionService, IABCUserSessionService>();

            ShellInteractionService.ModuleActivated += ModuleActivatedEventHandler;

            base.Run();
        }

        private void ModuleActivatedEventHandler(object sender, EventArgs e)
        {
            if (_first)
            {
                _first = false;
                EventTopic userSettingsTopic = WorkItem.EventTopics.Get(EventTopicNames.ShowChooseDefaultWarehouseDialog);
                userSettingsTopic.Fire(this, new EventArgs(), WorkItem, PublicationScope.Global);
            }
        }
                
        [EventSubscription(EventTopicNames.ShowDialog)]
        public void OnShowDialog(object sender, MenuItemExecutedEventArgs args)
        {
            if ((_abcUserSessionService.ClientId == null) || (_abcUserSessionService.WarehouseId == null))
            {
                OnChooseDefaultWarehouseDialog(this, null);
            }
            else
            {
                EventTopic itemTopic = WorkItem.RootWorkItem.EventTopics.Get(args.MenuItem.Parameters);
                itemTopic.Fire(this, new MenuEventArgs("", "", args.OpenInNewWindow, null), WorkItem, PublicationScope.Global);
            }
        }
        
        [EventSubscription(EventTopicNames.ShowChooseDefaultWarehouseDialog)]
        public void OnChooseDefaultWarehouseDialog(object sender, EventArgs args)
        {
            try
            {
                IChooseDefaultWarehouseView chooseWarehouse = WorkItem.SmartParts.AddNew<ChooseDefaultWarehouseView>();
                
                if (chooseWarehouse.ShowDialog() == true)
                {
                    _abcUserSessionService.ClientId = chooseWarehouse.SelectedClientId;
                    _abcUserSessionService.WarehouseId = chooseWarehouse.SelectedWarehouseId;

                    EventTopic userSettingsTopic = WorkItem.EventTopics.Get(Imi.SupplyChain.UX.UXEventTopicNames.UserSettingsChangedTopic);

                    if (userSettingsTopic != null)
                    {
                        UserSettingsChangedEventArgs userSettingsChangedEventArgs = new UserSettingsChangedEventArgs(true);
                        userSettingsTopic.Fire(this, userSettingsChangedEventArgs, WorkItem, PublicationScope.Descendants);
                    }
                }
            }
            finally
            {
                if ((_abcUserSessionService.ClientId == null) || (_abcUserSessionService.WarehouseId == null))
                    ShellInteractionService.ShowMessageBox(LocalResources.ChangeUserSettings_Caption, LocalResources.ChangeUserSettings_Message, null, Infrastructure.MessageBoxButton.Ok, Infrastructure.MessageBoxImage.Warning);
            }
        }

